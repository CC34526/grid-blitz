function t(key, params = {}) {
  const parts = key.split('.');
  let str = TRANSLATIONS[currentLang] || TRANSLATIONS.en;
  for (const part of parts) {
    str = str?.[part];
    if (str === undefined) {
      str = TRANSLATIONS.en;
      for (const p of parts) str = str?.[p];
      break;
    }
  }
  if (typeof str !== 'string') return key;
  return Object.entries(params).reduce(
    (result, [name, value]) => result.replaceAll(`{${name}}`, String(value)),
    str
  );
}

function getSkinName(skinId) {
  return t(`skinNames.${skinId}`);
}

function getAbilityName(skinId) {
  return t(`abilityNames.${skinId}`);
}

function getAbilityDesc(skinId) {
  return t(`abilityDesc.${skinId}`);
}

function setLanguage(lang) {
  if (!TRANSLATIONS[lang]) lang = 'en';
  currentLang = lang;
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  applyStaticTranslations();
  if (typeof window.onLanguageChange === 'function') {
    window.onLanguageChange();
  }
}

function populateLanguageSelect() {
  const select = document.getElementById('language-select');
  if (!select) return;
  const current = select.value || currentLang;
  select.innerHTML = '';
  LANGUAGES.forEach(({ id, label }) => {
    const option = document.createElement('option');
    option.value = id;
    option.textContent = label;
    select.appendChild(option);
  });
  if (TRANSLATIONS[current]) select.value = current;
  else select.value = 'en';
}

function applyStaticTranslations() {
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll('[data-i18n-aria]').forEach((el) => {
    el.setAttribute('aria-label', t(el.dataset.i18nAria));
  });
}
